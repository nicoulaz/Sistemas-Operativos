# Algoritmo para calcular el área de un triángulo
base = float(input("Ingrese la base: ")) # Lectura de la base
altura = float(input("Ingrese la altura: ")) # Lectura de la altura
area = (base * altura) / 2 # Aplicación de la fórmula (B*H)/2
print(f"El área del triángulo es: {area}") # Impresión del área