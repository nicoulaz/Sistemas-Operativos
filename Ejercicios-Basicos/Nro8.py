# Algoritmo para obtener medidas de un rectángulo
largo = float(input("Ingrese el largo: ")) # Entrada del largo
ancho = float(input("Ingrese el ancho: ")) # Entrada del ancho
superficie = largo * ancho # Cálculo del área
perimetro = 2 * (largo + ancho) # Suma de todos los lados
print(f"Superficie: {superficie}") # Mostrar superficie
print(f"Perímetro: {perimetro}") # Mostrar perímetro