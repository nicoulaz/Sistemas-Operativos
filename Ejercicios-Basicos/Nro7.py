# Calcular costo de boleto por kilómetro recorrido
km = float(input("Kilómetros a recorrer: ")) # Distancia del viaje
costo_km = float(input("Costo por kilómetro: ")) # Tarifa por km
total_boleto = km * costo_km # Cálculo del precio
print(f"El costo del boleto es: {total_boleto}") # Mostrar precio