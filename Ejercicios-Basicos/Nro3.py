# Calcular salario basado en horas trabajadas y costo por hora
horas = float(input("Horas trabajadas: ")) # Ingreso de horas
costo = float(input("Costo por hora: ")) # Ingreso del valor por hora
sueldo = horas * costo # Multiplicación para obtener el total
print(f"El sueldo semanal es: {sueldo}") # Mostrar sueldo