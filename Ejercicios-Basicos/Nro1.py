# Algoritmo para calcular el promedio de tres notas
n1 = float(input("Ingrese la primera nota: ")) # Captura de la primera nota
n2 = float(input("Ingrese la segunda nota: ")) # Captura de la segunda nota
n3 = float(input("Ingrese la tercera nota: ")) # Captura de la tercera nota
promedio = (n1 + n2 + n3) / 3 # Suma de notas dividida entre el total
print(f"El promedio final es: {promedio}") # Salida del resultado